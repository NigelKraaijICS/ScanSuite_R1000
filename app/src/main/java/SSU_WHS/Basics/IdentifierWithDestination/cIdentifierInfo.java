package SSU_WHS.Basics.IdentifierWithDestination;

public class cIdentifierInfo {

    public String infoKeyStr;
    public String getInfoKeyStr() {
        return infoKeyStr;
    }

    public String infoValueStr;
    public String getInfoValueStr() {
        return infoValueStr;
    }

    public cIdentifierInfo(String pvInfoKeyStr, String pvInfoValueStr) {


        this.infoKeyStr = pvInfoKeyStr;
        this.infoValueStr = pvInfoValueStr;

    }

}
